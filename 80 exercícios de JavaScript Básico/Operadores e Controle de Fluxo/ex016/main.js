// Crie um programa que verifica se um número é positivo, negativo ou zero.

const numero = 1

if (numero > 0) {
    console.log('número positivo')
} else if (numero < 0) {
    console.log('número negativo')
} else if (numero == 0) {
    console.log('zero')
} else {
    console.log('número inválido')
}